package br.com.digitalhouse;

public class Aluno {

    private String nome;
    private String sobreNome;
    private Integer codigoDeAluno;

    public Aluno() {
    }

    public Aluno(String nome, String sobreNome, Integer codigoDeAluno) {
        this.nome = nome;
        this.sobreNome = sobreNome;
        this.codigoDeAluno = codigoDeAluno;
    }

}
